function bodyFor(section, title, index) {
  const level = levelFor(index);
  const kind = kindFor(index);
  const duration = durationFor(level, kind);
  const tags = [section.slug, slugify(title).split('-')[0], level, kind];
  const summary = `Learn ${title} through a safe, repeatable ${section.title.toLowerCase()} workflow.`;
  const outcomes = section.outcomes.map((item) => `- ${item}`).join('\n');
  const focus = focusBulletsFor(section.slug, title);

  return `---\ntitle: ${JSON.stringify(title)}\norder: ${10 + index}\nsummary: ${JSON.stringify(summary)}\ntags: ${JSON.stringify(tags)}\nlevel: ${level}\nkind: ${kind}\nduration: ${JSON.stringify(duration)}\n---\n\nimport Callout from '../../../components/Callout.astro';\n\n${marker}\n\nThis ${kind} is part of the **${section.title}** track. It focuses on **${title}** as a practical skill you can apply in labs, CTFs, and authorized assessments.\n\n<Callout type=\"warning\" title=\"Practice only where authorized\">\n  Use this material only in your own lab, a CTF, or an environment where you have explicit permission. Keep proofs minimal, reversible, and respectful of real users.\n</Callout>\n\n## What you will learn\n\n${outcomes}\n\n## The core idea\n\n${title} is useful when you can explain the system in front of you before you touch it. Start by naming the asset, the user, the trust boundary, and the expected control. Then compare the expected behavior with what the system actually does.\n\nFor this topic, write a one-sentence claim before testing: \"I expect this control to stop this user from doing this action.\" If the evidence contradicts the claim, you have something worth investigating. If it matches, record the result and move on.\n\n## Technique focus\n\n${focus}\n\n## Safe practice workflow\n\n1. Define the target and confirm it is allowed.\n2. Create or choose test data that belongs to you.\n3. Record the normal behavior before changing inputs or state.\n4. Change one variable at a time and compare the response.\n5. Save only the evidence needed to explain the behavior.\n6. Write the likely fix or defensive control in plain language.\n\n<Callout type=\"tip\" title=\"Lab exercise\">\n  ${section.lab}\n</Callout>\n\n## Checklist\n\n- Can you describe the security boundary without naming a tool?\n- Do you have a clean baseline request, file, log entry, or screenshot?\n- Did you avoid destructive actions and real user data?\n- Can another learner reproduce your observation from your notes?\n- Can you state the impact and the fix in one paragraph?\n\n## Checkpoint\n\nBefore moving on, write three lines in your notes: what you expected, what you observed, and what you would test next. That habit matters more than memorizing a payload because it scales across targets and technologies.\n`;
}

let written = 0;
let skipped = 0;

for (const section of sections) {
  for (const [index, title] of section.topics.entries()) {
    const file = join(contentRoot, section.slug, `${slugify(title)}.mdx`);
    const content = bodyFor(section, title, index);

    mkdirSync(dirname(file), { recursive: true });
    if (existsSync(file)) {
      const existing = readFileSync(file, 'utf8');
      if (!existing.includes(marker)) {
        skipped += 1;
        continue;
      }
    }

    writeFileSync(file, content, 'utf8');
    written += 1;
  }
}

